function parseModules(raw: string): ModuleData[] {
  const moduleKeys = ['MODULE 1', 'MODULE 2', 'MODULE 3']
  const modules: ModuleData[] = []
  const contentStart = raw.indexOf('---\n\nMODULE 1')
  const courseBody = raw.slice(contentStart === -1 ? 0 : contentStart)
  const allLessons: Lesson[] = []
  const lessonRegex = /LESSON (\d+\.\d+)\n([^\n]+)\n([\s\S]*?)(?=\nLESSON \d+\.\d+|$)/g
  let match

  while ((match = lessonRegex.exec(courseBody)) !== null) {
    const [, id, title, content] = match
    allLessons.push({ id, title: title.trim(), content: content.trim() })
  }

  for (let i = 0; i < moduleKeys.length; i++) {
    const moduleIntro = moduleIntros[moduleKeys[i]]
    const moduleNumber = String(i + 1)
    const lessons = allLessons.filter((lesson) => lesson.id.startsWith(`${moduleNumber}.`))

    modules.push({
      id: moduleKeys[i],
      title: moduleIntro.title,
      intro: '',
      quote: moduleIntro.quote,
      milestone: moduleIntro.milestone,
      lessons,
    })
  }

  return modules
}

